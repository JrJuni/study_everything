#include <iostream> // 입출력 스트림 라이브러리를 포함합니다. 화면에 글자를 출력하려면 필요해요.

int main() { // C++ 프로그램은 항상 'main' 함수에서 시작해요. 여기가 프로그램의 출발점이죠!
    // std::cout은 'standard output stream'의 약자로, 화면(콘솔)에 데이터를 출력하는 역할을 해요.
    // << 연산자는 오른쪽에 있는 데이터를 왼쪽의 스트림으로 보냅니다. (여기서는 화면으로 보내죠)
    // "Hello, C++ World!"는 화면에 출력하고 싶은 문자열이에요.
    // std::endl은 'end line'의 약자로, 줄바꿈을 해주는 역할을 해요. (마치 엔터 키를 누른 것처럼요)
    std::cout << "Hello, C++ World!" << std::endl;

    // 프로그램이 성공적으로 종료되었다는 것을 운영체제에 알려주기 위해 0을 반환해요.
    return 0;
}